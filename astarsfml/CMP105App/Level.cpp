// Render level
void Level::render()
{
	
	bool finished = false;
	while (!finished)
	{
		for (auto& item : coord)
		{
			beginDraw();
			tileMap.render(window);
	//		std::cout << item.x << " , " << item.y << std::endl;
			child.setPosition(item.x * 15,item.y * 15);
			window->draw(child);
			Sleep(100);
			endDraw();
		}
		finished = true;
	}
	
	
	return;
}

